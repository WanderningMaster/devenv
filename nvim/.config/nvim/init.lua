-- delve project root conf
vim.keymap.set('n', '<leader>db', function()
  local file = vim.fn.expand '%'
  local line = vim.fn.line '.'
  local git_root = vim.fn.systemlist('git rev-parse --show-toplevel')[1]

  if git_root and git_root ~= '' then
    local relative_path = vim.fn.fnamemodify(file, ':.')
    local full_path = git_root .. '/' .. relative_path .. ':' .. line
    vim.fn.setreg('+', full_path) -- Copy to clipboard
    print('Copied: ' .. full_path)
  else
    print 'Not inside a Git project!'
  end
end, { desc = 'Copy file location with project root', noremap = true, silent = true })

